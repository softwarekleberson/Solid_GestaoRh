package model;

public class Terceirizado {

	private String nomeResponsavelObra;
	
	public Terceirizado(String nomeResponsavelObra) {
		
		this.nomeResponsavelObra = nomeResponsavelObra;
	}

	public String getNomeResponsavelObras() {
		return nomeResponsavelObra;
	}

	public void setNomeResponsavelObra(String nome) {
		this.nomeResponsavelObra = nome;
	}
	
}
